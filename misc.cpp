string codeName(string str1, string str2)
{
  string input = str1 + str2;
  string code_name;

  // Convert to lowercase and copy non-space characters
  for (char c : input) {
    if (c != ' ') {  // Check if the character is not a space
        code_name += std::tolower(c);  // Convert character to lowercase and add to output
    }
  }

return code_name;


}
